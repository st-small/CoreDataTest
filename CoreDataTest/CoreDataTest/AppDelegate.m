- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions {
    // Override point for customization after application launch.
    
    
    SiSStudent* std = [NSEntityDescription insertNewObjectForEntityForName:@"SiSStudent" inManagedObjectContext:self.managedObjectContext];
    std.firstName = @"Vasiliy";
    std.lastName = @"Lily";
    std.age = 17;
    
    //NSArray* words = @[@"kjfdshjghfd", @"jhdskfh", @"VasEk"];
    //[std addStudentKeyWord:[NSSet setWithArray:words]];
    
    NSMutableSet* words = [NSMutableSet set];
    SiSKeyWords* word = [NSEntityDescription insertNewObjectForEntityForName:@"SiSKeyWords" inManagedObjectContext:self.managedObjectContext];
    word.keyWord = @"aaa";
    [words addObject:word];
    SiSKeyWords* word2 = [NSEntityDescription insertNewObjectForEntityForName:@"SiSKeyWords" inManagedObjectContext:self.managedObjectContext];
    word2.keyWord = @"bbb";
    [words addObject:word2];
    
    [std addStudentKeyWord:words];
    
    [std.managedObjectContext save:nil];
    
    NSFetchRequest* rqst = [[NSFetchRequest alloc] init];
    NSEntityDescription* dsc = [NSEntityDescription entityForName:@"SiSStudent" inManagedObjectContext:self.managedObjectContext];
    [rqst setEntity:dsc];
    
    NSError* err = nil;
    
    NSArray* arr = [self.managedObjectContext executeFetchRequest:rqst error:&err];
    
    NSLog(@"%@", arr);
    
    for (SiSStudent* std in arr) {
        NSLog(@"%@ - %@ - %ld - %@", std.firstName, std.lastName, (long)std.age, std.studentKeyWord);
    }
    
    return YES;
}


